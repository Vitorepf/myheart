  function gerarCantada() {
    var language = document.getElementById("language").value;
    var cantadasIdioma = cantadas[language];
    var index = Math.floor(Math.random() * cantadasIdioma.length);
    var cantada = cantadasIdioma[index];
  
    var cantadaTexto = document.getElementById("cantadaTexto");
  
    cantadaTexto.textContent = cantada.texto;
  
    var cantadaMediaContainer = document.getElementById("cantadaMediaContainer");
    cantadaMediaContainer.innerHTML = '';
  
    var media = getRandomMedia();
  
    if (media.type === 'image') {
      var img = document.createElement("img");
      img.src = media.src;
      img.alt = "Cantada Image";
      img.classList.add("cantadaMedia");
      cantadaMediaContainer.appendChild(img);
    } else if (media.type === 'video') {
      var video = document.createElement("video");
      video.src = media.src;
      video.autoplay = true;
      video.controls = true;
      video.playsinline = true;
      video.classList.add("cantadaMedia");
      cantadaMediaContainer.appendChild(video);
      player = new Plyr(video);
    }
  
    var card = document.querySelector('.card');
    card.classList.remove('show');
    setTimeout(function() {
      card.classList.add('show');
    }, 300);
  }
  
  function changeLanguage() {
    gerarCantada();
  }
  
